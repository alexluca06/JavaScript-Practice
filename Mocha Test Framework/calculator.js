class Calculator {

    add(number1, number2) {
        return number1 + number2;
    }

    sub(number1, number2) {
        return number1 - number2;
    }

    mul(number1, number2) {
        return number1 * number2;
    }

    div(number1, number2) {
        return number1 / number2;
    }
}

module.exports = Calculator;